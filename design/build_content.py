def balance_positions(questions):
    """正解位置が①に偏らないよう、選択肢をシャッフルして正解位置を均等化する。
    単一正解の問題は正解位置をラウンドロビンで散らし、複数正解はランダムに並べ替える。
    correctAnswers と wrongChoiceExplanations のキーも新しい位置へ正しく付け替える。"""
    counter = 0
    for q in questions:
        n = len(q["choices"])
        correct = list(q["correctAnswers"])
        if len(correct) == 1:
            target = counter % n
            counter += 1
            src = correct[0]
            others = [i for i in range(n) if i != src]
            random.shuffle(others)
            new_order, oi = [], 0
            for pos in range(n):
                if pos == target:
                    new_order.append(src)
                else:
                    new_order.append(others[oi]); oi += 1
        else:
            new_order = list(range(n))
            random.shuffle(new_order)
        old_to_new = {old: pos for pos, old in enumerate(new_order)}
        q["choices"] = [q["choices"][old] for old in new_order]
        q["correctAnswers"] = sorted(old_to_new[c] for c in correct)
        q["wrongChoiceExplanations"] = {
            str(old_to_new[int(k)]): v for k, v in q["wrongChoiceExplanations"].items()
        }
    return questions

