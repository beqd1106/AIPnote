def build_lessons_aip(all_qs):
    """service へのキーワード最長一致で本問・ドリルをサブレッスンへ割当てる。
    最長一致にすることで GPT-4o(→l-gen6) と単独GPT(→l-gen5) を正しく分ける。
    未一致は同分野の先頭レッスンへフォールバック（件数を表示・0が理想）。"""
    from collections import defaultdict
    dom_subs = defaultdict(list)
    for sl in SUBLESSONS:
        sl["_quiz"], sl["_drill"] = [], []
        dom_subs[sl["domain"]].append(sl)

    def best(q):
        svc = q.get("service") or ""
        subs = dom_subs.get(q["domain"], [])
        best_sl, best_len = None, -1
        for sl in subs:
            for kw in sl["keywords"]:
                if kw in svc and len(kw) > best_len:
                    best_sl, best_len = sl, len(kw)
        return best_sl, subs

    fallback = 0
    for q in all_qs:
        sl, subs = best(q)
        if sl is None:
            if not subs:
                continue
            sl = subs[0]; fallback += 1
        key = "_drill" if "ドリル" in q.get("tags", []) else "_quiz"
        sl[key].append(q["id"])

    lessons = []
    for sl in SUBLESSONS:
        lessons.append({
            "id":sl["id"],"title":sl["title"],"domain":sl["domain"],
            "estimatedMinutes":sl["minutes"],"summary":sl["summary"],
            "sections":sl["sections"],"quizIds":sl["_quiz"],
            "challengeQuizIds":None,
            "drillQuizIds":sl["_drill"] if sl["_drill"] else None,
        })
    print(f"レッスン: {len(lessons)}本／フォールバック割当 {fallback}件")
    return lessons

