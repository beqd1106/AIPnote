VALID = {"aiBasics", "genAI", "trends", "ethics", "prompt"}
def validate(qs):
    errs, seen = [], set()
    for q in qs:
        i = q.get("id", "?")
        if i in seen: errs.append(f"重複id {i}")
        seen.add(i)
        if q.get("domain") not in VALID: errs.append(f"{i} 不正domain {q.get('domain')}")
        ch = q.get("choices", [])
        if len(ch) != 4: errs.append(f"{i} choices={len(ch)}")
        ca = q.get("correctAnswers", [])
        if not ca: errs.append(f"{i} correctAnswers空")
        for c in ca:
            if not (0 <= c < len(ch)): errs.append(f"{i} 不正correct {c}")
        for k in q.get("wrongChoiceExplanations", {}):
            ki = int(k)
            if not (0 <= ki < len(ch)): errs.append(f"{i} 不正wrongkey {k}")
            if ki in ca: errs.append(f"{i} wrongkeyが正解を指す {k}")
        # ドリルは短問のため誤答解説の完備は必須にしない（本問のみ必須）
        if "ドリル" not in q.get("tags", []):
            for idx in range(len(ch)):
                if idx not in ca and str(idx) not in q.get("wrongChoiceExplanations", {}):
                    errs.append(f"{i} 選択肢{idx}の誤答解説なし")
        if not q.get("explanation"): errs.append(f"{i} explanation空")
        if q.get("difficulty") not in (1, 2, 3): errs.append(f"{i} difficulty={q.get('difficulty')}")
    return errs

# --- 選択肢の書き換えパッチ（design/fix_choices*.json）---
# 「正解だけ文章が長い＝消去法で当てられる」癖を是正するため、選択肢の文言だけを差し替える。
# 形式：[{"id": "q-xx-001", "choices": [4つ]}]。順序は元データのまま（＝correctAnswers・
# wrongChoiceExplanations のindexは不変）。この後の balance_positions で位置は均等化される。
_fixed = 0
for ff in sorted(glob.glob(os.path.join(DESIGN, "fix_choices*.json"))):
    fixes = {f["id"]: f["choices"] for f in json.load(open(ff, encoding="utf-8"))}
    for q in questions:
        new = fixes.pop(q["id"], None)
        if new is None:
            continue
        if len(new) != len(q["choices"]):
            raise SystemExit(f"fix適用エラー {q['id']}: 選択肢数が {len(q['choices'])} → {len(new)}")
        q["choices"] = new
        _fixed += 1
    if fixes:
        raise SystemExit(f"fix適用エラー: 存在しないid {sorted(fixes)[:5]}")
if _fixed:
    print(f"選択肢パッチ適用: {_fixed}問")

